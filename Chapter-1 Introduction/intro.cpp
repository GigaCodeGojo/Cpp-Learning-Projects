#include <iostream>  // This line includes the iostream library, which provides input and output functionality.

using namespace std; // This line tells the compiler to use the standard namespace, which includes commonly used functions and objects.

int main() {  // This is the main function where the program execution begins.
    
    // The next line is a comment. Comments are ignored by the compiler and are used to provide explanations to the code.
    // This program will print a simple message to the console.
    
    cout << "Hello, World!" << endl;  // This line uses the cout (console output) stream to display "Hello, World!" followed by a newline.
    
    return 0;  // The main function returns an integer value (0) to indicate successful execution.
}

